// Завдання 1: Селектори
// a. Всі елементи <p> мають мати текст "Змінений текст".
$("p").text("Змінений текст");

// b. Елементу з ідентифікатором task1 додаємо клас highlight.
$("#task1").addClass("highlight");

// c. Всі елементи <li> мають мати клас list-item.
$("li").addClass("list-item");

// Завдання 2: Зміни стилів
// a. Змінюємо колір тексту всіх елементів з класом highlight на червоний.
$(".highlight").css("color", "red");

// b. Змінюємо фон всіх елементів з класом list-item на світло-сірий.
$(".list-item").css("background-color", "lightgray");

// Завдання 3: Анімація
// Робимо анімаційний ефект для елементів з класом highlight.
$(".highlight").fadeIn(2000); // наприклад, fadeIn або slideDown

// Завдання 4: Зміна вмісту
// Змінюємо текст останнього елемента <p> у блоку з ідентифікатором task1.
$("#task1 p:last").text("Новий останній текст");

// Завдання 5: Додаткове завдання
// Створюємо кнопку на сторінці та додаємо обробник подій.
$("<button>").text("Видалити елементи").click(function() {
    $(".list-item").remove();
}).appendTo("body");
